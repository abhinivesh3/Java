package DataTypes;

public abstract class Variables {
    public static void main(String arg[]){
        byte b = 127;
        short s = 300;
        int i = 100;
        float f = 25.3f;
        char c = 'A';
        System.out.println("byte : "+b);
        System.out.println("short : "+s);
        System.out.println("int : "+i);
        System.out.println("float : "+f);
        System.out.println("char : "+c);
    }
}
